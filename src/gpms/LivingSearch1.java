 // function to return users arraylist with particular data 
    public ArrayList<LivingClass> ListUsers(String ValToSearch)
    {
        ArrayList<LivingClass> usersList = new ArrayList<LivingClass>();
        
        Statement st;
        ResultSet rs;
        
        try{
            Connection con = getConnection();
            st = con.createStatement();
            String searchQuery = "SELECT * FROM `livingtable` WHERE CONCAT(fullname,address,regdate,regno,taluka)LIKE'%"+ValToSearch+"%'";
            rs = st.executeQuery(searchQuery);
            
            LivingClass ls;
            
            while(rs.next())
            {
                ls = new LivingClass(
                                 rs.getString("fullname"),
                                 rs.getString("address"),
                                 rs.getDate("regdate"),
                                 rs.getInt("regno"),
                                 rs.getString("taluka")
                                );
                usersList.add(ls);
            }
            
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
        
        return usersList;
    }
    
