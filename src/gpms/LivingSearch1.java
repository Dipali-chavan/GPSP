    public void findUsers()
    {
       //jTable_Users.removeAll();
        ArrayList<LivingClass> users = ListUsers(jText_Search.getText());
        
        model.setColumnIdentifiers(new Object[]{"FullName","Address","RegDate","RegNo","Taluka"});
        Object[] row = new Object[5];
        
        for(int i = 0; i < users.size(); i++)
        {
            row[0] = users.get(i).getFullName();
            row[1] = users.get(i).getAddress();
            row[2] = users.get(i).getRegDate();
            row[3] = users.get(i).getRegNo();
            row[4] = users.get(i).getTaluka();
            model.addRow(row);
        }
       
       jTable_Users.setModel(model);
       
    }
