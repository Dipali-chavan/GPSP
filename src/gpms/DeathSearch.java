    public ArrayList<DeathClass> ListUsers(String ValToSearch)
    {
        ArrayList<DeathClass> usersList = new ArrayList<DeathClass>();
        
        Statement st;
        ResultSet rs;
        
        try{
            Connection con = getConnection();
            st = con.createStatement();
            String searchQuery = "SELECT * FROM `deathtable` WHERE CONCAT(name,dob,placeofdeath,permanantadd,regno,regdate,mothername,fathername) LIKE '%"+ValToSearch+"%'";
            rs = st.executeQuery(searchQuery);
            
            DeathClass deathclass;
            //BirthClass b;
            
            while(rs.next())
            {
               deathclass = new DeathClass(
                                 
                                 rs.getString("name"),
                                 rs.getDate("dob"),
                                 rs.getString("placeofdeath"),
                                 rs.getString("permanantadd"),
                                 rs.getInt("regno"),
                                 rs.getDate("regdate"),
                                 rs.getString("mothername"),
                                 rs.getString("fathername")
               
                                );
               usersList.add(deathclass);
            }
            
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
        
        return usersList;
    }
    
