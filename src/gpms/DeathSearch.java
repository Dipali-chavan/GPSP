    public void findUsers()
    {
        ArrayList<DeathClass> users = ListUsers(jText_search.getText());
        
        model.setColumnIdentifiers(new Object[]{"Fullname","Dateofdeath","PlaceofDeath","PermanantAdd","Regno","RegDate","MotherName","FatherName"});
        Object[] row = new Object[8];
        
        for(int i = 0; i < users.size(); i++)
        {
            row[0] = users.get(i).getFullname();
            row[1] = users.get(i).getDateofdeath();
            row[2] = users.get(i).getPlaceofDeath();
            row[3] = users.get(i).getPermanantAdd();
            row[4] = users.get(i).getRegno();
            row[5] = users.get(i).getRegDate();
            row[6] = users.get(i).getMotherName();
            row[7] = users.get(i).getFatherName();
            
        model.addRow(row);
            
        }
       jTable_Users.setModel(model);
       
    }

