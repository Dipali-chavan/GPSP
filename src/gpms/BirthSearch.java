 // function to return users arraylist with particular data 
    public ArrayList<BirthClass> ListUsers(String ValToSearch)
    {
        ArrayList<BirthClass> usersList = new ArrayList<BirthClass>();
        
        Statement st;
        ResultSet rs;
        
        try{
          con = getConnection();
            st = con.createStatement();
            String searchQuery = "SELECT * FROM `birthtable` WHERE CONCAT(firstname,lastname,dob,timeofbirth,placeofbirth,city,taluka,district,gender,regno,regdate,mothername,fathername) LIKE '%"+ValToSearch+"%'";
            rs = st.executeQuery(searchQuery);
            
            BirthClass birthclass;
            //BirthClass b;
            
            while(rs.next())
            {
               birthclass = new BirthClass(
                                 
                                 rs.getString("firstname"),
                                 rs.getString("lastname"),
                                 rs.getDate("dob"),
                                 rs.getTime("timeofbirth"),
                                 rs.getString("placeofbirth"),
                                 rs.getString("city"),
                                 rs.getString("taluka"),
                                 rs.getString("district"),
                                 rs.getString("gender"),
                                 rs.getInt("regno"),
                                 rs.getDate("regdate"),
                                 rs.getString("mothername"),
                                 rs.getString("fathername")
               
                                );
               usersList.add(birthclass);
            }
            
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
        
        return usersList;
    }
    
