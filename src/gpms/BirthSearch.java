    public void findUsers()
    {
        ArrayList<BirthClass> users = ListUsers(jText_search.getText());
     
        model.setColumnIdentifiers(new Object[]{"Fname","Lname","DOB","Birth_time","Birth_place","City"," Taluka","District","Gender","Regno"," RegDate","Mother_name","Father_name"});
        Object[] row = new Object[13];
        
        for(int i = 0; i < users.size(); i++)
        {
            row[0] = users.get(i).getFname();
            row[1] = users.get(i).getLname();
            row[2] = users.get(i).getDOB();
            row[3] = users.get(i).getBirth_time();
            row[4] = users.get(i).getBrith_place();
            row[5] = users.get(i).getCity();
            row[6] = users.get(i).getTaluka();
            row[7] = users.get(i).getDistrict();
            row[8] = users.get(i).getGender();
            row[9] = users.get(i).getRegno();
            row[10] = users.get(i).getRegDate();
            row[11] = users.get(i).getMother_name();
            row[12] = users.get(i).getFather_name();
        model.addRow(row);
            
        }
       jTable_Users.setModel(model);
       
    }
